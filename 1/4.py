def findJudge(n: int, trust) -> int:
    if n == 1:
        return 1  # Если есть только один человек, он автоматически судья.

    trust_counts = [0] * (n + 1)  # Массив для подсчета доверия

    # Обрабатываем список доверия
    for a, b in trust:
        trust_counts[a] -= 1  # Если человек доверяет кому-то, он не может быть судьей
        trust_counts[b] += 1  # Увеличиваем счетчик доверия для человека, которому доверяют

    for i in range(1, n + 1):
        if trust_counts[i] == n - 1:  # Судье доверяют все остальные (n-1), а он никому
            return i

    return -1  # Если судья не найден